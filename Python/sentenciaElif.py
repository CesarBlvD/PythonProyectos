print("Convertir numero a letras")

num1 = int(input("Ingresa un numero a convertir: "))
if num1 == 1:
    print("El numero que ingresaste es: Uno")
elif num1 == 2:
    print("El numero que ingresaste es: Dos")
elif num1 == 3:
    print("El numero que ingresaste es: Tres")
elif num1 == 4:
    print("El numero que ingresaste es: Cuatro")
elif num1 == 5:
    print("El numero que ingresaste es: Cinco")
else:
    print("Este programa solo puede leer hasta el numero 5")

print("Fin.")
